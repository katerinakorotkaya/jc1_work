package my.primitive;

public class PrimitiveObject {
    public static void main(String[] args) {

        Boolean b = true;
        Byte b1 = 1;
        Short s = 2;
        Integer i = 3;
        Long l = 4L;
        Float f = 5.0F;
        Double d = 6.0;
        Character c = '7'; // autoboxing to object type
        char c2 = c; // unboxing
    }
}
